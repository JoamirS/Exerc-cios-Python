def calculate_quadratic_equation(j, o, m, r):
    # Calculate a solution for a quadratic equation
    # There are always two solutions to a quadratic equation, solution_x1 and solution_x2
    solution_xr1 = (-o+(o**2-4*j*m)**(1/2))/(2*j)
    solution_xr2 = (-o-(o**2-4*j*m)**(1/2))/(2*j)

    return solution_xr1, solution_xr2
