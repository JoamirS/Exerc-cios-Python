print(f'{variable}')

# deixando um espaço de 10 posições para a direita
print(f'{variable:>10}')

# deixando um espaço de 10 posições para a esquerda
print(f'{variable:<10}')

# deixando a variáveil no centro
print(f'{variable:^10}')

